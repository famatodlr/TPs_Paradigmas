package submarino;

public class Points {

    public static int x;
    public static int y;
    public static Coordenate initialPosition;

    public Points(int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
